    private void btnSacuvajKupcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajKupcaActionPerformed
        String imePrezime = txtImePrezime.getText();
        String email = txtEmail.getText();
        String adresa = txtAdresa.getText();
        String maticniBr = txtMaticniBr.getText();
        String ziroRacun = txtZiroRacun.getText();
        Mesto m = (Mesto) cmbMesto.getSelectedItem();

        if (imePrezime.isEmpty() || email.isEmpty() || adresa.isEmpty() || maticniBr.isEmpty() || ziroRacun.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite sve podatke");
            return;
        } else {
            if(!email.contains("@")){
                JOptionPane.showMessageDialog(this, "Email nije dobar.");
                return;
            }
            
            Kupac k = new Kupac(-1, imePrezime, email, maticniBr, ziroRacun, adresa, m);
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.SACUVAJ_KUPCA);
            kz.setParametar(k);

            try {
                KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
                ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();

                if (so.getIzuzetak() == null) {
                    JOptionPane.showMessageDialog(this, "Sistem je zapamtio kupca");
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti kupca");
                    throw new Exception(so.getIzuzetak());
                }

            } catch (IOException ex) {
                Logger.getLogger(NoviKupac.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(NoviKupac.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnSacuvajKupcaActionPerformed

