    public void srediTabelu() {
        try {

            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.VRATI_KUPOVINE);

            KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
            ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();

            List<OpstiDomenskiObjekat> listaKupovinaBaza = (List<OpstiDomenskiObjekat>) so.getOdgovor();

            List<Kupovina> listaFiltriranihKupovina = new ArrayList<>();

            if (checkKriterijum.isSelected()) {
                String kriterijum = txtKriterijumPretrage.getText().trim();

                for (OpstiDomenskiObjekat o : listaKupovinaBaza) {
                    Kupovina k = (Kupovina) o;
                    Date dat = k.getDatum();
                    SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
                    if (String.valueOf(k.getKupovinaID()).contains(kriterijum) || k.getKupac().getImePrezime().toLowerCase().contains(kriterijum.toLowerCase()) || k.getNapomena().toLowerCase().contains(kriterijum.toLowerCase()) || String.valueOf(k.getUkupno()).contains(kriterijum) || sdf.format(dat).contains(kriterijum.toLowerCase())) {
                        listaFiltriranihKupovina.add(k);
                    }
                }
                if (listaFiltriranihKupovina.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje kupovine po zadatoj vrednosti.");
                   
                } else {
                    JOptionPane.showMessageDialog(this, "Sistem je nasao kupovine po zadatoj vrednosti.");
                    
                }
            } else {
                for (OpstiDomenskiObjekat o : listaKupovinaBaza) {
                    Kupovina k = (Kupovina) o;
                    listaFiltriranihKupovina.add(k);
                }
            }

            ModelTabeleKupovine mtk = new ModelTabeleKupovine(listaFiltriranihKupovina);
            tabelaKupovine.setModel(mtk);

        } catch (IOException ex) {
            Logger.getLogger(PretragaKupaca.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PretragaKupaca.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
