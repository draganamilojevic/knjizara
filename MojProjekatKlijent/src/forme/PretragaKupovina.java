    private void btnPrikaziDetaljeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziDetaljeActionPerformed

        int red = tabelaKupovine.getSelectedRow();
        if (red != -1) {
            ModelTabeleKupovine mtk = (ModelTabeleKupovine) tabelaKupovine.getModel();
            Kupovina k = mtk.vratiMiIzabranuKupovinu(red);
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.VRATI_STAVKE);
            kz.setParametar(k);
            try {
                KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
                ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
                List<OpstiDomenskiObjekat> listaS = (List<OpstiDomenskiObjekat>) so.getOdgovor();
                List<StavkaKupovine> listaSK = new ArrayList<>();
                for (OpstiDomenskiObjekat odo : listaS) {
                    StavkaKupovine sk = (StavkaKupovine) odo;
                    listaSK.add(sk);
                }
                k.setListaStavki(listaSK);

            } catch (IOException ex) {
                Logger.getLogger(PretragaKupovina.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(PretragaKupovina.class.getName()).log(Level.SEVERE, null, ex);
            }
            NovaKupovina nova = new NovaKupovina();
            nova.setKupovina(k);
        
            nova.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Nije odabrana kupovina");
        }
    }//GEN-LAST:event_btnPrikaziDetaljeActionPerformed

