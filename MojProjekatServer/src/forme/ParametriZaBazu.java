    private void popuniPostojecimParametrima() {
        try {
            String driver = PropertyReader.getInstanca().getValue("driver");
            String url = PropertyReader.getInstanca().getValue("url");
            String user = PropertyReader.getInstanca().getValue("user");
            String password = PropertyReader.getInstanca().getValue("password");
            String port = PropertyReader.getInstanca().getValue("port");

            txtDriver.setText(driver);
            txtUrl.setText(url);
            txtUser.setText(user);
            txtPassword.setText(password);
            txtPort.setText(port);

            btnSacuvaj.setEnabled(false);

            txtDriver.setEditable(false);
            txtUrl.setEditable(false);
            txtUser.setEditable(false);
            txtPassword.setEditable(false);
            txtPort.setEditable(false);

        } catch (IOException ex) {
            Logger.getLogger(ParametriZaBazu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
