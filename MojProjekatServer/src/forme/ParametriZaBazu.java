    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String driver = txtDriver.getText();
        String url = txtUrl.getText();
        String user = txtUser.getText();
        String password = txtPassword.getText();
        String port = txtPort.getText();
        
        try {
            PropertyReader.getInstanca().setValue("driver", driver);
            PropertyReader.getInstanca().setValue("url", url);
            PropertyReader.getInstanca().setValue("user", user);
            PropertyReader.getInstanca().setValue("password", password);
            PropertyReader.getInstanca().setValue("port", port);
            JOptionPane.showMessageDialog(null, "Uspesno sacuvani parametri!");
            dispose();
        } catch (IOException ex) {
            Logger.getLogger(ParametriZaBazu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Greska! Parametri nisu sacuvani!");
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

